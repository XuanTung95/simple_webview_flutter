class FullScreenWebview extends StatefulWidget {
  const FullScreenWebview({Key? key}) : super(key: key);

  @override
  State<FullScreenWebview> createState() => _FullScreenWebviewState();
}

class _FullScreenWebviewState extends State<FullScreenWebview> {
  late WebViewController webViewController;
  final TextEditingController _urlController = TextEditingController();

  @override
  void initState() {
    super.initState();

    /// Replace WebViewPlatform.instance with the modified version
    if (Platform.isAndroid) {
      SimpleAndroidWebViewPlatform.registerWith();
    }
    webViewController = WebViewController();
    webViewController.loadRequest(Uri.parse("https://google.com"));
    webViewController.setJavaScriptMode(JavaScriptMode.unrestricted);
    webViewController.setNavigationDelegate(NavigationDelegate(onNavigationRequest: (NavigationRequest request) {
      ScaffoldMessenger.of(context).showSnackBar(SnackBar(
        content: Text("Nav ${request.url}", maxLines: 1,),
      ));
      return NavigationDecision.navigate;
    }, onPageFinished: (_) {
      print("onPageFinished() callback");
    }));
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      floatingActionButton: FloatingActionButton(onPressed: () {  },),
      appBar: AppBar(
        title: Text("Full screen WebView"),
        actions: [
          NavigationControls(webViewController: this.webViewController),
          SampleMenu(webViewController: this.webViewController),
        ],
      ),
      body: Stack(
        children: [
          SizedBox.expand(
            child: WebViewWidget(controller: webViewController),
          ),
        ],
      ),
    );
  }
}

